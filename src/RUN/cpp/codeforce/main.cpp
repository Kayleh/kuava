int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int tt;
    cin >> tt;
    vector<vector<int>> nums;
    vector<int> tolSum;
    for (int i = 0; i < tt; i++)
    {
        int n;
        vector<int> tmp;
        int curSum = 0;
        for (int j = 0; j < 3; j++)
        {
            int t;
            cin >> t;
            tmp.push_back(t);
            curSum += t;
        }
        nums.push_back(tmp);
        tolSum.push_back(curSum);
    }

    const int _idiff = 5, _toldiff = 10;
    int res = 0;
    for (int i = 0; i < tt; i++)
    {
        for (int j = i + 1; j < tt; j++)
        {
            bool ok = true;
            int totaldiff = abs(tolSum[i] - tolSum[j]);
            if (totaldiff <= _toldiff)
            {
                for (int k = 0; k < nums[i].size(); k++)
                {
                    int idiff_ = abs(nums[i][k] - nums[j][k]);
                    if (idiff_ > _idiff)
                    {
                        ok = false;
                        break;
                    }
                }
                if (ok)
                    res++;
            }
        }
    }
    cout << res << endl;
    return 0;
}

